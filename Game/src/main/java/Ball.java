public class Ball {
    private int size;
    private int speed;

    public Ball() {
    }

    public Ball(int size, int speed) {
        this.size = size;
        this.speed = speed;
    }

    public int getSpeed() {
        return speed;
    }

    public void setSpeed(int speed) {
        this.speed = speed;
    }

    public Ball(int size) {
        this.size = size;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }
}
